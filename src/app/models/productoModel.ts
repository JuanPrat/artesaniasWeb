export class producto{
    id:string;
    nombre: string;
    precio: number;
    descripcion:string;
    materiales:string;
    imgUrl:string;

    constructor(){
        this.id = this.id;
        this.descripcion = this.descripcion;
        this.precio = this.precio;
        this.nombre = this.nombre;
        this.materiales = this.materiales;
        this.imgUrl = this.imgUrl;
    }
}